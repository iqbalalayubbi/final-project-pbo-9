    private void startTimer(){
        // Create a timer that triggers every 2 second
        timer = new Timer(2000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Call the function to update the table model
                updateTableModel();
            }
        });
        // Start the timer
        timer.start();
    }
    
